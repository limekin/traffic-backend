    <h2>About Us</h2>

    <p>
        We are a group of young students who are aware of the ignorance the
        people of ouy locality have in keeping safety measures, and following
        traffic rules.

        This gave us the much needed motivation to come up with something
        that can help reduce or regulate these violations that happen a lot
        in our locality.

        Therefore, we as the students of CS department developed an android
        application that can be used by the public to report these kind
        of violations whenever, and whevever they see them.

        The reported violations are either tracked with the help of the traffic
        police if it has enough details to identify the guy, or they are
        discarded if they don't have enough detials to identify the suspect.
